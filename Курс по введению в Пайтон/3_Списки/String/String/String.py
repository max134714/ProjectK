string = 'string'

print(string[0])#первый элемент 
print(string[2])#второй
print(string[-1])#последний

print(string[2:5])#от второго по пятый
print(string[:5])#пять последних 
print(string[2:])#все  кроме двух первых
print(string[::2])#шаг 2
print(string[::-1])#наоборот

print(string[2] + string)#склеивание строк
